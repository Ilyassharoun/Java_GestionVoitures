    private void validateFields() throws IllegalArgumentException {
    // Check required fields
    if (txtPlaque.getText().trim().isEmpty()) {
        txtPlaque.requestFocus();
        throw new IllegalArgumentException("La plaque est obligatoire");
    }
    
    if (txtMarque.getText().trim().isEmpty()) {
        txtMarque.requestFocus();
        throw new IllegalArgumentException("La marque est obligatoire");
    }
    
    if (txtModel.getText().trim().isEmpty()) {
        txtModel.requestFocus();
        throw new IllegalArgumentException("Le modèle est obligatoire");
    }
    
    // Validate numeric fields
    try {
        int annee = Integer.parseInt(txtAnnee.getText().trim());
        if (annee < 1900 || annee > java.time.Year.now().getValue()) {
            throw new IllegalArgumentException("L'année doit être entre 1900 et " + java.time.Year.now().getValue());
        }
    } catch (NumberFormatException e) {
        txtAnnee.requestFocus();
        throw new IllegalArgumentException("L'année doit être un nombre valide");
    }
    
    try {
        int km = Integer.parseInt(txtKilometrage.getText().trim());
        if (km < 0) throw new IllegalArgumentException("Le kilométrage ne peut pas être négatif");
    } catch (NumberFormatException e) {
        txtKilometrage.requestFocus();
        throw new IllegalArgumentException("Le kilométrage doit être un nombre valide");
    }
    
    try {
        int dernierVidange = Integer.parseInt(txtDernierVidange.getText().trim());
        int kilometrage = Integer.parseInt(txtKilometrage.getText().trim());
        if (dernierVidange > kilometrage) {
            throw new IllegalArgumentException("Le dernier vidange ne peut pas être supérieur au kilométrage actuel");
        }
    } catch (NumberFormatException e) {
        txtDernierVidange.requestFocus();
        throw new IllegalArgumentException("Le dernier vidange doit être un nombre valide");
    }
}

