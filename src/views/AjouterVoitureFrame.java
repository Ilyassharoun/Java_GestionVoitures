    private void BTN_ENGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_ENGActionPerformed
        // TODO add your handling code here:
         try {
        // 1. Validate all fields first
        validateFields();
        
        // 2. Create vehicle object
        Voiture v = new Voiture(
            txtPlaque.getText().trim().toUpperCase(),
            txtMarque.getText().trim(),
            txtModel.getText().trim(),
            Integer.parseInt(txtAnnee.getText().trim()),
            txtCouleur.getText().trim(),
            Integer.parseInt(txtKilometrage.getText().trim()),
            Integer.parseInt(txtDernierVidange.getText().trim())
        );

        // 3. Check for duplicate plaque (only in add mode)
        if (originalPlaque == null && VoitureDAO.vehicleExists(v.getPlaqueImmatriculation())) {
            throw new IllegalArgumentException("Une voiture avec cette plaque existe déjà");
        }

        // 4. Save or update
        if (originalPlaque == null) {
            VoitureDAO.addVoiture(v);
            showSuccess("Véhicule ajouté avec succès!");
        } else {
            VoitureDAO.modifierVoiture(originalPlaque, v);
            showSuccess("Véhicule modifié avec succès!");
        }
        
        this.dispose();
        new GestionDesVoituresFrame().setVisible(true);
        
    } catch (NumberFormatException e) {
        showError("Valeur numérique invalide", "Veuillez entrer un nombre valide pour:\n"
            + (txtAnnee.getText().isEmpty() ? "- Année\n" : "")
            + (txtKilometrage.getText().isEmpty() ? "- Kilométrage\n" : "")
            + (txtDernierVidange.getText().isEmpty() ? "- Dernière vidange" : ""));
    } catch (IllegalArgumentException e) {
        showError("Données invalides", e.getMessage());
    } catch (SQLException e) {
        showError("Erreur de base de données", "Une erreur est survenue:\n" + e.getMessage());
    }
    }//GEN-LAST:event_BTN_ENGActionPerformed

